public void prepareView(){
        cbFieldDay.removeAllItems();
        cbFieldMonth.removeAllItems();
        cbFieldYear.removeAllItems();
        
        for (int i = 1; i < 32; i++) {
            cbFieldDay.addItem(i+"");
    }
        for (int i = 1; i < 13; i++) {
            cbFieldMonth.addItem(i+"");
    }
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy");
        Date date = new Date();
        int year = Integer.parseInt(sdf.format(date));
        
        for (int i = 1950; i < year; i++) {
            cbFieldYear.addItem(i+"");
    }
}





