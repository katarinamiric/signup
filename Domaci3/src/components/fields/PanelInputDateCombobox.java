    @Override
    public Object getValue() {
        String day = (String)cbFieldDay.getSelectedItem();
        String month = (String)cbFieldMonth.getSelectedItem();
        String year = (String)cbFieldYear.getSelectedItem();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        
        try {
            return sdf.parse(day + "/" + month + "/" + year);
        } catch (ParseException ex) {
            return "n/a";
        }
    }

    @Override
    public void initialize(Object initValue) {
        List<String> items = (List<String>) initValue;
        lblFieldText.setText(items.get(0));
        lblError.setText(items.get(1));
    }

    @Override
    public void setLabels(String lbl1, String lbl2) {
        lblFieldText.setText(lbl1);
        lblError.setText(lbl2);
    }
